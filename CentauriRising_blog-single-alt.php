				<h3 class="page-title">Music</h3>
				<p class="lead">
				The Battlefield theme carries a lot of emotional strength with fans of the series. The theme was first created for Battlefield 1942, and used again in some shape for each next installment of the franchise, with Battlefield 1 being the only exception.
				</p>
				<p class="lead">
				Expertly using the theme again, this time similar to how the piece was orchestrated for Battlefield 1943 (no PC version for this one!), DICE underpins every single intense moment of arguably the best Battlefield trailer. If you watch the trailer again, try to be conscious about how many beats match what’s happening visually. This naturally happens a lot in most trailers and indeed any sort of serious video editing. Here DICE takes it to the extreme, and instead of ending up feeling overdone, the elegance and production value of the sequences and how they flow into each other make it something else. From impacts and explosions to camera swipes and scene transitions, the music relentlessly pushes the edit forward, mimicking the state of flow during gameplay in one of those matches where everything satisfyingly comes together.
				</p>
				<p class="lead">
				DICE is doing a great job of scripting action sequences to match the music, especially with explosions, there’s 17 instances of impacts and explosions that exactly match a beat in the music, with just a few that don’t. This isn’t to say that this should be a main objective when making a trailer, but in this case it’s a running theme that was successfully pulled off.
				</p>
			</div>

